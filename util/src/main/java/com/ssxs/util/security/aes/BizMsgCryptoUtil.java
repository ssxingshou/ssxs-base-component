package com.ssxs.util.security.aes;

import org.apache.commons.codec.binary.Base64;

import javax.crypto.Cipher;
import javax.crypto.spec.IvParameterSpec;
import javax.crypto.spec.SecretKeySpec;
import java.nio.charset.Charset;
import java.util.Arrays;
import java.util.Random;

/**
 * 提供消息的加解密接口(UTF8编码的字符串).
 * <ol>
 * <li>加密消息</li>
 * <li>对消息进行解密。</li>
 * </ol>
 * 说明：异常java.security.InvalidKeyException:illegal Key Size的解决方案
 * <ol>
 * <li>在官方网站下载JCE无限制权限策略文件（JDK7的下载地址：
 * http://www.oracle.com/technetwork/java/javase
 * /downloads/jce-7-download-432124.html</li>
 * <li>下载后解压，可以看到local_policy.jar和US_export_policy.jar以及readme.txt</li>
 * <li>如果安装了JRE，将两个jar文件放到%JRE_HOME%\lib\security目录下覆盖原来的文件</li>
 * <li>如果安装了JDK，将两个jar文件放到%JDK_HOME%\jre\lib\security目录下覆盖原来文件</li>
 * </ol>
 */
public class BizMsgCryptoUtil {
    static Charset CHARSET = Charset.forName("utf-8");
    static Base64 base64 = new Base64();
    static byte[] aesKey;

    // 生成4个字节的网络字节序
    static byte[] getNetworkBytesOrder(int sourceNumber) {
        byte[] orderBytes = new byte[4];
        orderBytes[3] = (byte) (sourceNumber & 0xFF);
        orderBytes[2] = (byte) (sourceNumber >> 8 & 0xFF);
        orderBytes[1] = (byte) (sourceNumber >> 16 & 0xFF);
        orderBytes[0] = (byte) (sourceNumber >> 24 & 0xFF);
        return orderBytes;
    }

    // 还原4个字节的网络字节序
    static int recoverNetworkBytesOrder(byte[] orderBytes) {
        int sourceNumber = 0;
        for (int i = 0; i < 4; i++) {
            sourceNumber <<= 8;
            sourceNumber |= orderBytes[i] & 0xff;
        }
        return sourceNumber;
    }

    // 随机生成16位字符串
    static String getRandomStr() {
        String base = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789";
        Random random = new Random();
        StringBuffer sb = new StringBuffer();
        for (int i = 0; i < 16; i++) {
            int number = random.nextInt(base.length());
            sb.append(base.charAt(number));
        }
        return sb.toString();
    }

    /**
     * 对明文进行加密.
     * @param randomStr
     * @param encodingAesKey
     * @param text 需要加密的明文
     * @return 加密后base64编码的字符串
     * @throws AesException
     *             aes加密失败
     */
    private static String encrypt(String randomStr, String encodingAesKey, String text) throws AesException {
        if (encodingAesKey.length() != 43) {
            throw new AesException(AesException.IllegalAesKey);
        }
        aesKey = Base64.decodeBase64(encodingAesKey + "=");

        ByteGroup byteCollector = new ByteGroup();
        byte[] randomStrBytes = randomStr.getBytes(CHARSET);
        byte[] textBytes = text.getBytes(CHARSET);
        byte[] networkBytesOrder = getNetworkBytesOrder(textBytes.length);

        // randomStr + networkBytesOrder + text
        byteCollector.addBytes(randomStrBytes);
        byteCollector.addBytes(networkBytesOrder);
        byteCollector.addBytes(textBytes);

        // ... + pad: 使用自定义的填充方式对明文进行补位填充
        byte[] padBytes = PKCS7Encoder.encode(byteCollector.size());
        byteCollector.addBytes(padBytes);

        // 获得最终的字节流, 未加密
        byte[] unencrypted = byteCollector.toBytes();

        try {
            // 设置加密模式为AES的CBC模式
            Cipher cipher = Cipher.getInstance("AES/CBC/NoPadding");
            SecretKeySpec keySpec = new SecretKeySpec(aesKey, "AES");
            IvParameterSpec iv = new IvParameterSpec(aesKey, 0, 16);
            cipher.init(Cipher.ENCRYPT_MODE, keySpec, iv);

            // 加密
            byte[] encrypted = cipher.doFinal(unencrypted);

            // 使用BASE64对加密后的字符串进行编码
            String base64Encrypted = base64.encodeToString(encrypted);

            return base64Encrypted;
        } catch (Exception e) {
            throw new AesException(AesException.EncryptAESError);
        }
    }

    /**
     * 对密文进行解密.
     *
     * @param encodingAesKey
     * @param text 需要解密的密文
     * @return 解密得到的明文
     * @throws AesException
     *             aes解密失败
     */
    private static String decrypt(String encodingAesKey, String text) throws AesException {
        if (encodingAesKey.length() != 43) {
            throw new AesException(AesException.IllegalAesKey);
        }
        aesKey = Base64.decodeBase64(encodingAesKey + "=");

        byte[] original;
        try {
            // 设置解密模式为AES的CBC模式
            Cipher cipher = Cipher.getInstance("AES/CBC/NoPadding");
            SecretKeySpec key_spec = new SecretKeySpec(aesKey, "AES");
            IvParameterSpec iv = new IvParameterSpec(Arrays.copyOfRange(aesKey, 0, 16));
            cipher.init(Cipher.DECRYPT_MODE, key_spec, iv);

            // 使用BASE64对密文进行解码
            byte[] encrypted = Base64.decodeBase64(text);

            // 解密
            original = cipher.doFinal(encrypted);
        } catch (Exception e) {
            throw new AesException(AesException.DecryptAESError);
        }

        String infoContent;
        try {
            // 去除补位字符
            byte[] bytes = PKCS7Encoder.decode(original);

            // 分离16位随机字符串,网络字节序和AppId
            byte[] networkOrder = Arrays.copyOfRange(bytes, 16, 20);

            int infoLength = recoverNetworkBytesOrder(networkOrder);

            infoContent = new String(Arrays.copyOfRange(bytes, 20, 20 + infoLength), CHARSET);
        } catch (Exception e) {
            throw new AesException(AesException.IllegalBuffer);
        }

        return infoContent;

    }

    /**
     * 将消息加密打包.
     * <ol>
     * <li>对要发送的消息进行AES-CBC加密</li>
     * </ol>
     *
     * @param infoMsg
     *            需要加密传输的消息，字符串
     *
     * @return 加密后的字符串
     * @throws AesException
     *             执行失败，请查看该异常的错误码和具体的错误信息
     */
    public static String encryptMsg(String aesKey, String infoMsg) throws AesException {
        return encrypt(getRandomStr(), aesKey, infoMsg);
    }

    /**
     * 获取解密后的明文.
     * <ol>
     * <li>对消息进行解密</li>
     * </ol>
     *
     * @param postData
     *            密文，对应request请求的数据
     *
     * @return 解密后的原文
     * @throws AesException
     *             执行失败，请查看该异常的错误码和具体的错误信息
     */
    public static String decryptMsg(String aesKey, String postData) throws AesException {
        return decrypt(aesKey, postData);
    }

}